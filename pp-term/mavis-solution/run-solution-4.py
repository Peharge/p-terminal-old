import subprocess
import sys

# Farbcodes definieren
red = "\033[91m"
green = "\033[92m"
yellow = "\033[93m"
blue = "\033[94m"
magenta = "\033[95m"
cyan = "\033[96m"
white = "\033[97m"
black = "\033[30m"
orange = "\033[38;5;214m"
reset = "\033[0m"
bold = "\033[1m"

def print_message(message, color, is_bold=False):
    """Hilfsfunktion zum Drucken von Nachrichten mit Farbe und optionalem Fettdruck"""
    bold_text = bold if is_bold else ""
    print(f"{color}{bold_text}{message}{reset}")

def get_user_input():
    """Benutzereingabe abfragen und validieren"""
    while True:
        try:
            print(f"\nSolution Extension Information:\n-------------------------------")
            user_input = input(f"Do you want to start Solution Extension? [y/n]:").strip().lower()
            if user_input in ['y', 'yes']:
                return True
            elif user_input in ['n', 'no']:
                return False
            else:
                print_message("Invalid input. Please enter 'y/yes' or 'n/no'.", red, True)
        except (EOFError, KeyboardInterrupt):
            print_message("Input interrupted. Exiting the program.", red, True)
            sys.exit(1)

def execute_installation():
    """Installation ausführen und Fehlerbehandlung"""
    try:
        subprocess.run([sys.executable, "solution\\solution-main-4.py"], check=True)
        print_message("Solution Extension completed successfully.", blue, True)
    except subprocess.CalledProcessError:
        print_message("An error occurred while running the Solution Extension script.", red, True)
        sys.exit(1)
    except FileNotFoundError:
        print_message("The Solution Extension script 'solution-main-3.py' was not found.", red, True)
        sys.exit(1)
    except Exception as e:
        print_message(f"An unexpected error occurred: {str(e)}", red, True)
        sys.exit(1)

def main():
    """Hauptfunktion, die das Programm ausführt"""
    if get_user_input():
        execute_installation()
    else:
        print_message("Solution Extension was declined. Exiting the program.", blue, True)
        sys.exit(0)

if __name__ == "__main__":
    main()
